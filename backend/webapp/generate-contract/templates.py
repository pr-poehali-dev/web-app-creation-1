def _party_name(user: dict) -> str:
    utype = user.get("userType", "individual")
    if utype in ("legal-entity",):
        return user.get("companyName", "___________")
    elif utype == "entrepreneur":
        inn = user.get("inn", "")
        name = f"{user.get('lastName', '')} {user.get('firstName', '')} {user.get('middleName', '')}".strip()
        return f"Индивидуальный предприниматель {name}, ИНН {inn}"
    elif utype == "self-employed":
        name = f"{user.get('lastName', '')} {user.get('firstName', '')} {user.get('middleName', '')}".strip()
        return f"Самозанятый {name}"
    else:
        name = f"{user.get('lastName', '')} {user.get('firstName', '')} {user.get('middleName', '')}".strip()
        return name or "___________"


def _requisites(user: dict) -> str:
    lines = []
    utype = user.get("userType", "individual")
    name = f"{user.get('lastName', '')} {user.get('firstName', '')} {user.get('middleName', '')}".strip()

    if utype == "legal-entity":
        lines.append(f"Наименование: {user.get('companyName', '___________')}")
        if user.get("ogrn"):
            lines.append(f"ОГРН: {user['ogrn']}")
        if user.get("inn"):
            lines.append(f"ИНН: {user['inn']}")
        if user.get("directorName"):
            lines.append(f"Директор: {user['directorName']}")
    elif utype == "entrepreneur":
        lines.append(f"ИП {name}")
        if user.get("ogrnip"):
            lines.append(f"ОГРНИП: {user['ogrnip']}")
        if user.get("inn"):
            lines.append(f"ИНН: {user['inn']}")
    else:
        lines.append(name or "___________")
        if user.get("inn"):
            lines.append(f"ИНН: {user['inn']}")

    if user.get("legalAddress"):
        lines.append(f"Адрес: {user['legalAddress']}")
    elif user.get("city"):
        lines.append(f"Адрес: {user.get('region', '')} {user['city']}".strip())

    if user.get("phone"):
        lines.append(f"Телефон: {user['phone']}")
    if user.get("email"):
        lines.append(f"Email: {user['email']}")

    lines.append("")
    lines.append("Подпись: _______________________")
    lines.append(f"/{name or '___________'}/")
    lines.append("")
    lines.append("М.П.")

    return "\n".join(lines)