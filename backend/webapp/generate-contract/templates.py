def get_category_clauses(category: str) -> dict:
    return CATEGORY_CLAUSES.get(category, CATEGORY_CLAUSES["other"])


def format_date(d: str) -> str:
    try:
        parts = d.split("-")
        months = ["января", "февраля", "марта", "апреля", "мая", "июня",
                  "июля", "августа", "сентября", "октября", "ноября", "декабря"]
        return f"{int(parts[2])} {months[int(parts[1]) - 1]} {parts[0]} г."
    except Exception:
        return d


